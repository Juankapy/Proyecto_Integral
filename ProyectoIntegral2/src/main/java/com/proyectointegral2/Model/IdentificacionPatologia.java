package com.proyectointegral2.Model;

public class IdentificacionPatologia {
    private int idPerro;
    private int idPatologia;
    private String descripcion;

    public int getIdPerro() { return idPerro; }
    public void setIdPerro(int idPerro) { this.idPerro = idPerro; }

    public int getIdPatologia() { return idPatologia; }
    public void setIdPatologia(int idPatologia) { this.idPatologia = idPatologia; }

    public String getDescripcion() { return descripcion; }
    public void setDescripcion(String descripcion) { this.descripcion = descripcion; }
}